using Soenneker.Gen.Adapt.Tests.Dtos;
using Soenneker.Tests.Unit;
using AwesomeAssertions;
using Xunit;

namespace Soenneker.Gen.Adapt.Tests;

/// <summary>
/// Verifies that Soenneker.Gen.EnumValues and Soenneker.Gen.Adapt work together in the same project:
/// - Both source generators run and produce code without conflict.
/// - Adapt enum parsing (GenAdapt_EnumParsers) still works for C# enums.
/// - Adapt mappings work for DTOs that use EnumValue-generated types.
/// </summary>
[Collection("Collection")]
public sealed class EnumValuesIntegrationTests : UnitTest
{
    public EnumValuesIntegrationTests(ITestOutputHelper output) : base(output)
    {
    }

    [Fact]
    public void Both_Generators_Coexist_Adapt_Enum_Parsing_Works()
    {
        // GenAdapt_EnumParsers is generated by Soenneker.Gen.Adapt for C# enums.
        // If both generators conflict or build fails, this would not compile/run.
        var source = new StringToEnumSource { StatusString = "Active" };
        var result = source.Adapt<StringToEnumDest>();

        result.Should()
              .NotBeNull();
        result.StatusString.Should()
              .Be(TestStatus.Active);
    }

    [Fact]
    public void Both_Generators_Coexist_EnumValues_FromValue_Works()
    {
        // TestOrderStatus.FromValue is generated by Soenneker.Gen.EnumValues.
        // Proves EnumValues generator ran and generated API is available.
        TestOrderStatus status = TestOrderStatus.FromValue(1);
        status.Should()
              .Be(TestOrderStatus.Pending);
        status.Value.Should()
              .Be(1);
    }

    [Fact]
    public void Adapt_Maps_EnumValue_Type_Same_Type()
    {
        // Adapt maps DTOs where the property type is an EnumValue-generated type (same type on both sides).
        var source = new EnumValueTypeSource { Status = TestOrderStatus.Completed };

        var result = source.Adapt<EnumValueTypeDest>();

        result.Should()
              .NotBeNull();
        result.Status.Should()
              .Be(TestOrderStatus.Completed);
        result.Status.Value.Should()
              .Be(2);
    }

    [Fact]
    public void Adapt_Maps_EnumValue_Type_Pending()
    {
        var source = new EnumValueTypeSource { Status = TestOrderStatus.Pending };

        var result = source.Adapt<EnumValueTypeDest>();

        result.Should()
              .NotBeNull();
        result.Status.Should()
              .Be(TestOrderStatus.Pending);
    }

    [Fact]
    public void EnumValues_List_And_Adapt_Work_Together()
    {
        // Use EnumValues-generated List to iterate, then use one value in an Adapt mapping.
        foreach (TestOrderStatus status in TestOrderStatus.List)
        {
            var source = new EnumValueTypeSource { Status = status };
            var result = source.Adapt<EnumValueTypeDest>();
            result.Status.Should()
                  .Be(status);
        }
    }

    [Fact]
    public void CSharp_Enum_And_EnumValue_Type_Both_Used_In_Same_Project()
    {
        // C# enum (TestStatus) -> Adapt generates Parse_TestStatus; EnumValue type (TestOrderStatus) -> EnumValues generates List/FromValue.
        var enumSource = new EnumSource { Status = TestStatus.Completed, Priority = 1 };
        var enumDest = enumSource.Adapt<EnumToStringDest>();
        enumDest.Status.Should()
                .Be("Completed");

        var orderSource = new EnumValueTypeSource { Status = TestOrderStatus.FromValue(2) };
        var orderDest = orderSource.Adapt<EnumValueTypeDest>();
        orderDest.Status.Should()
                 .Be(TestOrderStatus.Completed);
    }

    // --- EnumValue<string> (TestColorCode) ---

    [Fact]
    public void EnumValue_String_FromValue_Works()
    {
        TestColorCode color = TestColorCode.FromValue("R");
        color.Should()
             .Be(TestColorCode.Red);
        color.Value.Should()
             .Be("R");
    }

    [Fact]
    public void EnumValue_String_FromName_Works()
    {
        TestColorCode color = TestColorCode.FromName("Blue");
        color.Should()
             .Be(TestColorCode.Blue);
        color.Value.Should()
             .Be("B");
    }

    [Fact]
    public void Adapt_Maps_EnumValue_String_Type_Same_Type()
    {
        var source = new EnumValueStringTypeSource { Color = TestColorCode.Green };

        var result = source.Adapt<EnumValueStringTypeDest>();

        result.Should()
              .NotBeNull();
        result.Color.Should()
              .Be(TestColorCode.Green);
        result.Color.Value.Should()
              .Be("G");
    }

    [Fact]
    public void EnumValue_String_List_And_Adapt_Work_Together()
    {
        foreach (TestColorCode color in TestColorCode.List)
        {
            var source = new EnumValueStringTypeSource { Color = color };
            var result = source.Adapt<EnumValueStringTypeDest>();
            result.Color.Should()
                  .Be(color);
        }
    }

    // --- Same-type: identity vs copy (HasAccessibleParameterlessConstructor) ---

    [Fact]
    public void Adapt_EnumValue_same_type_returns_same_reference_TestOrderStatus()
    {
        // EnumValue types have no public parameterless ctor → generator emits "return source" (identity).
        var source = new EnumValueTypeSource { Status = TestOrderStatus.Completed };
        var result = source.Adapt<EnumValueTypeDest>();

        result.Status.Should().BeSameAs(source.Status);
        result.Status.Should().Be(TestOrderStatus.Completed);
    }

    [Fact]
    public void Adapt_EnumValue_same_type_returns_same_reference_TestColorCode()
    {
        // EnumValue<string> same behavior: identity when same-type mapping.
        var source = new EnumValueStringTypeSource { Color = TestColorCode.Green };
        var result = source.Adapt<EnumValueStringTypeDest>();

        result.Color.Should().BeSameAs(source.Color);
        result.Color.Should().Be(TestColorCode.Green);
    }

    [Fact]
    public void Adapt_type_with_parameterless_ctor_same_type_returns_new_instance()
    {
        // When mapping T -> T at the root (item.Adapt<CopyableItem>()), types with accessible parameterless ctor get "new T()" and copy.
        var source = new CopyableItem { Id = 42, Name = "foo" };
        var result = source.Adapt<CopyableItem>();

        result.Should().NotBeSameAs(source);
        result.Id.Should().Be(42);
        result.Name.Should().Be("foo");
    }
}